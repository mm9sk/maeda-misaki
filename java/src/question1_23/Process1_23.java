package question1_23;

public class Process1_23 {
	// フィールドに各項目の変数を定義
	private String name;
	private double length;
	private int speed;

	// getterとsetter
	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setLenght(double length) {
		this.length = length;
	}

	public double getLenght() {
		return length;
	}

	public void setSpeed(int speed) {
		this.speed = speed;
	}

	public int getSpeed() {
		return speed;
	}

}